import math

# Pembulatan
# math.ceil() => Membulatkan ke atas bilangan bulat terdekat
print(math.ceil(4.2)) # 5

# math.floor() => Membulatkan ke bawah bilangan terdekat
print(math.floor(4.8)) # 4

# math.trunc() => Menghapus bagian desimal (Hanya mengambil integer)
print(math.trunc(8.9)) # 8

#Pangkat dan Akar
# math.pow(x, y) => Menghitung x pangkat y (hasil float)
print(math.pow(2,3)) # 8.0

#math.sqrt() => Menghitung akar kuadrat dari x
print(math.sqrt(16)) # 4 (Akar dari 16)